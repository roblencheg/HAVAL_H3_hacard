import { readFileSync, existsSync, mkdirSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');

const pngPath = resolve(repoRoot, 'docs', 'haval_h3.png');
const outDir = resolve(repoRoot, 'src', 'generated');
const outPath = resolve(outDir, 'default-image.ts');

if (!existsSync(pngPath)) {
  console.error(`ERROR: ${pngPath} not found`);
  process.exit(1);
}

const buf = readFileSync(pngPath);
const sizeKB = buf.length / 1024;

if (sizeKB > 500) {
  console.error(`ERROR: ${pngPath} is ${sizeKB.toFixed(1)} KB — exceeds 500 KB limit`);
  process.exit(1);
}

const base64 = buf.toString('base64');
const dataUri = `data:image/png;base64,${base64}`;

if (!existsSync(outDir)) {
  mkdirSync(outDir, { recursive: true });
}

const content = `// Auto-generated by scripts/generate-default-image.mjs
// Do not edit manually.
export const DEFAULT_VEHICLE_IMAGE = ${JSON.stringify(dataUri)};
`;

writeFileSync(outPath, content, 'utf-8');
console.log(`Generated ${outPath} (${sizeKB.toFixed(1)} KB base64)`);
